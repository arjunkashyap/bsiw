			<h2>ENVIS Centre</h2>
			<p class="subText">Realising the importance of environmental information, the Government of India established the Environmental Information System (ENVIS) in December 1982. ENVIS was designated as the National Focal Point (NFP) for INFOTERRA, a global environmental information network of the United Nations Environment Programme (UNEP). ENVIS was also designated as the Regional Service Centre (RSC) of INFOTERRA in 1985 for the South Asia Sub-Region countries.</p>
			<p class="subText">The focus of ENVIS since its inception has been on providing environmental information to decision makers, policy planners, scientists and engineers, research workers, etc. all over the country. Since the environment is a broad-ranging, multi-disciplinary subject, a comprehensive information system needs the effective participation of all the institutions that are actively engaged in work relating to it. A large number of nodes, known as ENVIS Centres, have been established in the ENVIS network. The focal point is the Ministry of Environment & Forests. The focal point and the ENVIS Centres have been assigned various responsibilities.</p>
			
			<p class="subText subtitle">The BSI’s ENVIS on Floral Diversity</p>
			<p class="subText">in 1994, the Ministry of Environment &amp; Forests approved the setting up of an ENVIS Centre on plant ecology at the Industrial Section, Indian Museum, Botanical Survey of India, Calcutta. In September 2002, this centre was renamed the ENVIS Centre on Floral Diversity, and it has been functioning since then from the Central National Herbarium Building, 3rd Floor, Botanical Survey of India, P.O. Botanic Garden, Howrah – 711 103.</p>
			<p class="subText">The mandate of this ENVIS Centre is systematic collection and compilation of data (mainly secondary data) on floral diversity and disseminating data. The ENVIS Centre disseminates data through its website. The information is immensely helpful to various academic and research institutes, research scholars and students as well as lay people who are interested in this field.</p>
			<p class="subText">The ENVIS Centre carries out a number of activities apart from those of the BSI:</p>
			<ul class="ulNeedBullets_about">
				<li><p class="subText">All the databases prepared by the ENVIS Centre are maintained by it on the website.</p></li>
				<li><p class="subText">A bibliography and abstracts of papers published on the flora of the different states of India are compiled annually. These are published online.</p></li>
				<li><p class="subText">A glossary of botanical terms and abbreviations has been prepared by the centre.</p></li>
				<li><p class="subText">State-wise lists (for 24 states) indicating the distribution of Threatened and Endemic taxa have already been uploaded. They will be sent to experts for review. The lists of threatened plant species from Delhi, Jharkhand, Madhya Pradesh and Uttar Pradesh, will be incorporated in the database during 2013–2014.</p></li>
				<li><p class="subText">The ENVIS Centre publishes a newsletter regularly.</p></li>
				<li><p class="subText">News clippings on issues related to the environment, biodiversity and conservation from various newspapers are uploaded to the website (News Watch Module).</p></li>
				<li><p class="subText">A database of experts of the Botanical Survey of India and other organisations has been created.</p></li>
			</ul>
