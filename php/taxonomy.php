			<p class="subText">The State Flora Analysis of Tamil Nadu, Karnataka, Himachal Pradesh, Arunachal Pradesh, Sikkim, Meghalaya and Uttar Pradesh have been completed and published whereas, the State Flora of Tripura, Goa, Daman &amp; Diu, Dadra &amp; Nagar Haveli, Rajasthan, Saurashtra, Madhya Pradesh, Maharashtra, Punjab have been published while those of Manipur, Andaman &amp; Nicobar Islands and West Bengal have been partly published and that of Mizoram and Jammu &amp; Kashmir are currently under publication. The Survey has also published a large number of District Floras.</p>
