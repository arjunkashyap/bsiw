			<h2>Floristic and taxonomic studies</h2>
			<p class="subText">The taxonomic studies so far conducted by the Survey for the preparation of National/State/District Floras, fragile ecosystems and revisionary studies on families/genera/tribes have resulted in the discovery of about 1500 taxa new to India and about 700 taxa new to science, including 26 new genera, such as Seshagiria, Amischophacelus, Janakia, Pauella, Jainia, Nicobariodendron, Santapaua, Awasthiella, Kanjaraum, Chandrasekharania, Aenhenrya, Brachycaulos, Silentvalleya, Kunstleria, Indotristicha, Kurziodendron, etc.</p>
