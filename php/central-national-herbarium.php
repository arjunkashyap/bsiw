			<h2>Central National Herbarium</h2>
			<p class="subText">The Central National Herbarium (CNH) is a national repository with about 2 million plant species. It is one of the largest herbaria in the world. William Roxburgh established this herbarium in 1795 at his office-cum-residence. This building, located on the bank of the river Hooghly, is now called Roxburgh House. In 1883 the herbarium was shifted to a specially built building. Since 1970 it has been housed in an air-conditioned building.</p>
			<p class="subText">The CNH has collections not only from India but also from other parts of the world. It maintains exchange relations with various reputed international herbaria. More than 15,000 type specimens, the Wallichian collections, with more than 12,000 specimens, and one of the few lithographed copies of Wallich’s catalogue are housed in this herbarium. Microfiches of the Linnean herbarium and 26 other important herbaria are also preserved here. Further, there is a large collection of coloured illustrations, with the 2583 coloured illustrations forming the Roxburgh Icones enjoying pride of place. Graphic material is continually added in the form of photo negatives and Cibachrome prints of types and authentic materials obtained from Kew Herbarium, England.</p>
			<p class="subText">Scientists at the CNH have been assigned the task of carrying out floristic surveys of West Bengal, Orissa, Bihar and Jharkhand as well as rich areas such as Sikkim, Bhutan, the Andaman & Nicobar Islands and certain pockets of the Eastern Ghats of Andhra Pradesh. Explorations in these areas have not only enriched the herbarium with recent collections but have also contributed to critical floristic accounts and publication of state floras and checklists. Most of the revisionary studies, one of the major thrust areas of the Botanical Survey of India, have also been conducted by scientists of the CNH for the Flora of India.</p>
			<p class="subText">The herbarium is the centre of taxonomic activities in India. It has a palynology unit with scanning electron microscopes. This unit has worked on the seed morphology of about 30 families of flowering plants and the spore morphology of several pteridophytes. The unit is presently engaged in the preparation of a pollen atlas of India.</p>
			<p class="subText">The library of the CNH has a very large number of old and rare books and journals.</p>
