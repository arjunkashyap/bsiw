			<h2>Indian Botanic Garden</h2>
			<p class="subText">For nearly two centuries this garden, which is the largest and oldest of this kind in South East Asia has been a premier Institution for botanical and horticultural research in India. The great aesthetic beauty of this garden with its picturesque vistas, and vast greeneries serve as a source of inspiration, education, research and recreation to millions of visitors to this garden throughout the year from India and abroad. </p>
			<p class="subText">Established in 1787 by Lieutenant Colonel Robert Kyd, this garden is situated on the west bank of the river Hooghly (Ganga). The garden covers an area of 273 acres. Its unique landscape design initiated by Sir George King in 1872 is considered to be one of the best in the botanic gardens of the world with undulated land surfaces, artificial lakes and moats interconnected with underground pipes receiving water from the river Hooghly. </p>
			<p class="subText">The garden was known as East India Company’s Garden or the ‘Company Bagan’ or Calcutta Garden and later as the Royal Botanic Garden which after independence was renamed as the ‘Indian Botanical Garden’ in 1950.It came under the management of the Botanical Survey of India on January 1, 1963.</p>
			<p class="subText">Over 12,000 trees and shrubs belonging to 1400 species together with thousands of herbaceous plants are in cultivation in the open in 25 Divisions, Glass houses, Green Houses and conservatories. The garden maintains the germplasm collection of Bamboos, Bougainvillea, Citrus, Jasmine, Pandanus, Water Lilies and has the richest collection of Palms (about 109 species) in whole of South East Asia. In addition succulents, Hibiscus, Ficus, Aromatic plants, Gymno- sperms (in two Pinetums), Creepers, Ferns and a number of floricultural and arboricultural plants are grown in its Flower Garden, National Orchidarium, Student Garden. Besides a large number of medicinal plants in its Medicinal Plant Garden named as ‘Charak Udyan’ enrich the garden.</p>
			<p class="subText">A few interesting plants of the garden worth to mention are Branched Palm, Bread Fruit Tree, Double Coconut, Giant Water Lilies, Krishnabot , Mad tree, Shivalinga tree, etc. In addition researches are also conducted on plant introduction, multiplication, horticultural aspects and conservation.</p>
			<p class="subText">The Great Banyan Tree (Ficus bengalensis L.) of the Indian Botanic Garden attracts millions of visitors every year. It looks like a miniature forest and is over 250 years old with 2800 prop roots covering an area of 1.5 hectares. The large palm house of this garden has several interesting plants including the Double Coconut [Lodoicea maldivica (Gmel.) Pers] which produces the largest known seeds in the whole plant kingdom.</p>
