			<p class="subText">The Industrial Section has been a part of the Botanical Survey of India since 1911. Its galleries have more than 20,000 exhibits arranged in various bays and sections, based on their uses: crude drugs, food, fodder, dyes, tannins, gums, narcotics, beverages, etc. The Industrial Section is also engaged in imparting visual education (organizing exhibitions, mobile demonstrations and lectures) about the role of plants in environmental protection, their importance in our daily life and the need for conserving them. It also maintains a herbarium of economic plants. The large volume of data compiled and used by George Watt for his <span class="itlc">Dictionary of Economic Products of India</span> is preserved and displayed in a series of ledgers at the museum.</p>
