				<h2>About Center</h2>
				<p class="subText">The Western Centre of Botanical Survey of India came into existence on 12th December, 1955. Initially it was housed in the Law College premises, Pune and later in 1958, shifted to the present site at 7, Koregaon Road, Pune. In those days jurisdiction of the circle was the entire western India, from Rajasthan in the north to Kerala in the South. Part of Madhya Pradesh and Laccadive and Minicoy group of Islands were also under this centre. </p><p class="subText">Due to the establishment of new centre offices at Jodhpur and Allahabad, jurisdiction of the centre is now restricted to Maharashtra, Goa, Karnataka, and Union Territories of Daman, Diu, Dadra and Nagar-Haveli. Area under the present jurisdiction shows tremendous variation in the ecological conditions from western coast line to the drier eastern plains through the rich forests of western ghats. Besides the herbarium the centre is well-equipped for taxonomical research with a well-established library, museum and an associated garden. </p>
