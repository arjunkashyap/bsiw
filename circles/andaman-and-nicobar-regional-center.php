						<h2>Herbarium</h2>
						<p class="subText">By carrying out plant exploration trips to different islands, a well reputed herbarium with an acronym ‘PBL’ has been developed. It has approximately c.22,000 plant specimens. In this herbarium about 140 families are represented, including 100 type collections. It comprises Historical specimens collected by some of the pioneering plant collectors viz., Col. Robert Kyd (1791)., J. W. Helfer (1839), W. S. Kurz (1866), David Prain (1889) and Sir George King (1890). The collections made so far indicate that flora of these islands is quite different from mainland and shows more affinities towards Malayan counterparts.</p>
