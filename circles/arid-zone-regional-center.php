				<h2>About Center</h2>
				<p class="subText">Established on 18th February, 1972 at Jodhpur with jurisdiction of Rajasthan and Gujarat States. The area under jurisdiction encompass three main biogeographic regions viz. Thar desert, Rann and Coastal areas and Semi-arid zone which separates Thar in the north and Coasts in the south, besides several physiographic regions. The main hill ranges which transect the area include Aravalli, Vindhyas and Sahyadri, with highest peak at Mt. Abu between Nilgiri in the south and Himalaya in the north. The important rivers include Luni, Banas, Mahi, Chambal, Sabarmati, Narmada and Tapti. The occurrence of fossil wood logs of Jurassic age remind the prehistoric floral composition of the area. The climatic and edaphic variabilities have further increased the heterogenic nature of the area.</p>
