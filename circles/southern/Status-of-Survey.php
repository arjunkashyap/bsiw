					<h2>Status of Survey</h2>
					<p class="subText">The history of botanical survey in this part of the country dates back to 1797 (J.P. Rottler). Since then, the areas under the present jurisdiction of the Southern Circle were explored by many eminent scientists including H.F.C. Cleghorn, Sir. W. Elliot, J.E. Koenig, J.D. Hooker, Wight, Beddome, Bourdillon, C.A. Barber, J.S. Gamble, Fyson and so on. Their collections were housed at the then “Madras Herbarium” (name from the place of its origin is retained even after its shifting to Coimbatore). With the reorganization of the Botanical Survey of India and subsequent establishment of the Southern Circle, the Madras Herabrium was handed over to it. Since its establishment in 1955, about 474 intensive explorations were conducted besides several minor explorations. These include many multidisciplinary explorations to Silent Valley, Sispara, Anaimudis, Agasthyamalais, Nilgiris, Araku Valley and Gulf of Mannar. As a result of these explorations most of the areas have been fairly to well explored. Out of the 67 districts in three states and two Union Territory under this Circle, about 43 districts are well explored (&gt;70%), about 20 districts are partially explored (40 – 70%) and about 4 districts are under explored (&lt;40%).</p>
					<p class="subText">These explorations have resulted in the collection of 84,651 field numbers and approx. 408776 specimens. </p>
