						<h2>Herbarium</h2>
						<p class="subText">Known by the acronym ASSAM, the herbarium developed around the nucleus of 45,000 specimens collected by Fischer, Perry, Kingdon Ward, Bor and Kanjilal etc.At present it has <em>c.</em> 2,60,000 herbarium specimens of vascular plants including 508 type specimens of angiosperms, pteridophytes, Bryophytes and Lichens representing the flora of the North Eastern India. Besides this, it contains <em>c.</em> 13,000 specimens of Lichens and 11,000 specimens of Bryophytes (Liverworts) collected from different states of the North East India. Some of the collections are more than a century old.</p>
