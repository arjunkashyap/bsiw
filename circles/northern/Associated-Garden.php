					<h2>Associated Garden</h2>
					<p class="subText">Situated about 180 km away, the Circle maintains two experimental gardens at Pauri: one at Nagdev Block, covering about 14 ha, and another at Khirsu, covering about 8 ha. The Nagdev block of the garden has been designated as “National Gymnosperm collections” and include species like Abies pindrow, Cedrus deodara, Cupressus torulosa, Pinus wallichiana, Juniperus, Taxodium, etc. Among the recently introduced important species are the medicinally important Taxus wallichiana, the `living fossil’ Ginkgo biloba and rare and endemic palm Trachycarpus takil.</p>
					<p class="subText">At Khirsoo, the forested area is dominated by old Quercus, Rhododendrons, and Berberis, with a host of epiphytic ferns, orchids, lichens and bryophytes and is being maintained as “Nature Reserve”.</p>
